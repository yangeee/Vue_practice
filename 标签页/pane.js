Vue.component("pane",{
	name:"pane",
	template:'\
	<div class="pane" v-show="show">\
		<slot></slot>\
	</div>',
	props:{
		name:{
			type:String
		},
		label:{
			type:String,
			default:""
		}
	},
	data:function(){
		return{
			show:true
		}
	},
	methods:{
		updateNav(){
			this.$parent.updateNav();
		}
	},
	watch:{
		label(){
			this.updateNav();//监听更新标题
		}
	},
	mounted(){
		this.updateNav();//初始化是调用一次
	}
})